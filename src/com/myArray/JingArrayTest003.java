package com.myArray;

public class JingArrayTest003 {
    public static void main(String[] args) {
        //定义数组
        int[] arr = {1,2,3};

        //输出数组名
        System.out.println(arr);//输出内存空间：[I@1b6d3586

        //输出数组中的元素
        System.out.println(arr[0]);//输出1
        System.out.println(arr[1]);//输出2
        System.out.println(arr[2]);//输出3
    }
}
